package com.mygdx.game.UI;

public class ButtonPosition {
    private float pos_X, pos_Y;

    public float getPos_X() { return pos_X; }

    public void setPos_X(float pos_X) { this.pos_X = pos_X; }

    public float getPos_Y() { return pos_Y; }

    public void setPos_Y(float pos_Y) { this.pos_Y = pos_Y; }

    public ButtonPosition(float pos_X, float pos_Y) {
        this.pos_X = pos_X;
        this.pos_Y = pos_Y;
    }
}
